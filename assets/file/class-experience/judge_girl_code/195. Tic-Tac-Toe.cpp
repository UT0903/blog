int a[3][3], n, turn = 0, x, y, i;
int main (){
	scanf("%d", &n);
	if (n == 0) printf("There is a draw.");
	else
		for (i = 0 ; i < n ; i++){
			scanf("%d %d", &x, &y);
			if (a[x][y] == 0 && x < 3 && y < 3 && x >= 0 && y >= 0 && turn % 2 == 0){
				a[x][y] = 1;
				turn++;
			}
			else if (a[x][y] == 0 && x < 3 && y < 3 && x >= 0 && y >= 0 && turn % 2 == 1){
				a[x][y] = 10;
				turn++;
			}
			
			
			if(a[0][0]+a[0][1]+a[0][2] == 3 || a[1][0]+a[1][1]+a[1][2] == 3 || a[2][0]+a[2][1]+a[2][2] == 3 || a[0][0]+a[1][1]+a[2][2] == 3 || a[2][0]+a[1][1]+a[0][2] == 3 || a[2][0]+a[1][0]+a[0][0] == 3 || a[2][1]+a[1][1]+a[0][1] == 3 || a[2][2]+a[1][2]+a[0][2] == 3 ){
				printf("Black wins.");
				break;
			}
			else if(a[0][0]+a[0][1]+a[0][2] == 30 || a[1][0]+a[1][1]+a[1][2] == 30 || a[2][0]+a[2][1]+a[2][2] == 30 || a[0][0]+a[1][1]+a[2][2] == 30 || a[2][0]+a[1][1]+a[0][2] == 30 || a[2][0]+a[1][0]+a[0][0] == 30 || a[2][1]+a[1][1]+a[0][1] == 30 || a[2][2]+a[1][2]+a[0][2] == 30){
				printf("White wins.");
				break;
			}
			else if( i == n-1){
				printf("There is a draw.");
				break;
			}
		}
}


